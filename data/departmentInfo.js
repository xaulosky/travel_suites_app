/**
 * Busca información del departamento por diferentes criterios
 * @param {string} sku - SKU del producto
 * @param {string} name - Nombre del producto
 * @param {number} productId - ID del producto
 * @returns {Object|null} Información del departamento o null
 */
export function getDepartmentInfo(sku, name, productId) {
    // Intentar buscar por SKU primero
    if (sku && DEPARTMENT_INFO[sku]) {
        return DEPARTMENT_INFO[sku];
    }

    // Intentar buscar por nombre (parcial)
    for (const key in DEPARTMENT_INFO) {
        if (name && name.includes(key)) {
            return DEPARTMENT_INFO[key];
        }
    }

    // Si no se encuentra, retornar null
    return null;
}
